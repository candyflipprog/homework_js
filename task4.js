let firstNum = 4;
let secondNum = 8;
let thirdNum = 5;

const summaryNum = ((firstNum ** 3) + (secondNum ** 3) + (thirdNum ** 3)) / 3;
const average = Math.abs(summaryNum);

console.log("Среднее арифметическое:", average);

const geomtricNum = (firstNum ** 2) * (secondNum ** 2) * (thirdNum ** 2);
const geometricResult = Math.cbrt(geomtricNum);
console.log("Среднее геометрическое:", geometricResult);
